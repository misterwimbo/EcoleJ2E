package bean;

public class Ecole 
{
	private int nbr_classe= 0;
	private int nbr_employes= 0;
	private int nbr_eleves=0;
	private String nom=null;
	private String prenom=null;
	private String adresse=null;
	private String tel=null;
	
	
	public int getNbr_classe() {
		return nbr_classe;
	}
	public void setNbr_classe(int nbr_classe) {
		this.nbr_classe = nbr_classe;
	}
	public int getNbr_employes() {
		return nbr_employes;
	}
	public void setNbr_employes(int nbr_employes) {
		this.nbr_employes = nbr_employes;
	}
	public int getNbr_eleves() {
		return nbr_eleves;
	}
	public void setNbr_eleves(int nbr_eleves) {
		this.nbr_eleves = nbr_eleves;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getPrenom() {
		return prenom;
	}
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
}

  
